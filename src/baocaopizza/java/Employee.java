    public void getData(String query)
    {
        nvList.clear();
        ResultSet rs = kn.LayDuLieu(query);
        
        try {
            while(rs.next())
            {
                NhanVien nv = new NhanVien(rs.getString("MaNV"), rs.getNString("TenNhanVien"), rs.getNString("NgaySinh"), rs.getNString("QueQuan")
                , rs.getNString("SoDienThoai"), rs.getNString("Email"), rs.getNString("LoaiNhanVien"));
                nvList.add(nv);
            }
        }
        catch(SQLException sql)
        {
                     
        }
        
        tableModel.setRowCount(0);
        
        for(NhanVien nv : nvList)
        {
            tableModel.addRow(new Object[] {
                nv.getMaNhanVien(), nv.getTenNhanVien(), nv.getNgaySinh(), nv.getQueQuan(), nv.getSoDienThoai()
                    , nv.getEmail(), nv.getLoaiNhanVien()
            });
        }
       
    }
    
   
