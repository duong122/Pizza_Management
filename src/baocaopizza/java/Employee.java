    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:             
        String query1 = String.format("select * from NhanVien where MaNV='%s'", txtId.getText());       
        ResultSet rs = kn.LayDuLieu(query1);
       
       try {
           Boolean isResultSetEmpty = rs.next();
           
           if(txtId.getText().equals(""))
           {
               JOptionPane.showConfirmDialog(null, "You Are Misssing Employee ID", "Announcement", JOptionPane.YES_OPTION);
           }
           else if(isResultSetEmpty == false)
            {
                 String query = String.format("insert into NhanVien values('%s', '%s', '%s', '%s', '%s', '%s', '%s')", 
                    txtId.getText(), txtName.getText(), txtDateOfBirth.getText(), txtAddress.getText()
                    , txtPhoneNumber.getText(), txtEmail.getText(), cmbTypeOfEmployee.getSelectedItem().toString());
                Boolean r = kn.ThucThi(query);
                if(r)
                    JOptionPane.showConfirmDialog(null, "Added Sucessfully", "Announcement", JOptionPane.YES_OPTION);
                else
                    JOptionPane.showConfirmDialog(null, "Added Failed", "Announcement", JOptionPane.YES_OPTION);
            }
            else
                JOptionPane.showConfirmDialog(null, "This id already exist", "Announcement", JOptionPane.YES_OPTION);
       }
       catch(SQLException sql)
       {
           return;
       }
       
       getData("select * from NhanVien");
    }//GEN-LAST:event_btnAddActionPerformed

