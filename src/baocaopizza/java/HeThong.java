    public void getData(String query)
    {
        SPList.clear();
        ResultSet rs = kn.LayDuLieu(query);
        
        try {
            while(rs.next())
            {
                SanPham sp = new SanPham(rs.getString("MaSP"), rs.getNString("TenSP"),rs.getFloat("DonGia"),rs.getNString("MoTa"),rs.getString("MaLoaiSP"));
                SPList.add(sp);
            }
        }
        catch(SQLException sql)
        {
                     
        }
        
        tableModel.setRowCount(0);
        
        for(SanPham sp : SPList)
        {
            tableModel.addRow(new Object[] {
                sp.getMaSP(), sp.getTenSP(), sp.getDonGia(), sp.getMoTa(), sp.getMaLoaiSP()
            });
        }  
    }
