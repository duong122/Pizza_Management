    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String query1 = String.format("select * from SanPham where MaSP='%s'", txtMa.getText());       
        ResultSet rs = kn.LayDuLieu(query1);
       
       try {
           Boolean isResultSetEmpty = rs.next();
           
           if(txtMa.getText().equals(""))
           {
               JOptionPane.showConfirmDialog(null, "You Are Misssing SanPham ID", "Announcement", JOptionPane.YES_OPTION);
           }
           else if(isResultSetEmpty == false)
            {
                 String query = String.format("insert into SanPham values('%s', '%s', '%s', '%s', '%s')", 
                    txtMa.getText(), txtTen.getText(), txtDonGia.getText(), txtMoTa.getText(), cmbMaLoaiSP.getSelectedItem().toString());
                Boolean r = kn.ThucThi(query);
                if(r)
                    JOptionPane.showConfirmDialog(null, "Added Sucessfully", "Announcement", JOptionPane.YES_OPTION);
                else
                    JOptionPane.showConfirmDialog(null, "Added Failed", "Announcement", JOptionPane.YES_OPTION);
            }
            else
                JOptionPane.showConfirmDialog(null, "This id already exist", "Announcement", JOptionPane.YES_OPTION);
       }
       catch(SQLException sql)
       {
           return;
       }
       
       getData("select * from SanPham");
    }//GEN-LAST:event_btnThemActionPerformed

