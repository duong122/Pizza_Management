    private void tb_SPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_SPMouseClicked
        // TODO add your handling code here:
        int selectRow = tb_SP.getSelectedRow();
        try{
            DefaultTableModel model = (DefaultTableModel)tb_SP.getModel();

            String id = model.getValueAt(selectRow, 0).toString();
//            String name = model.getValueAt(selectRow, 1).toString();
            String price = model.getValueAt(selectRow, 2).toString();
            
            // Mo jFDia lay so luong
            frmDiaNhapSoLuong frm = new frmDiaNhapSoLuong(this, true);
            frm.setVisible(true);
            int sl = frm.SoLuong;
            float thanhTien = sl * Float.parseFloat(price);


            DefaultTableModel tableModel;
            tableModel = (DefaultTableModel)tb_HoaDon.getModel();

            Object obj[] = new Object[5];
            obj[0] = tableModel.getRowCount() + 1;
            obj[1] = id;
            obj[2] = sl;
            obj[3] = price;
            obj[4] = thanhTien;


            tableModel.addRow(obj);
            txtTongTien.setText(String.valueOf(tongTien()));
        }
        catch(Exception e){
            System.out.println("Loi mouseClicked");
        }
        
    }//GEN-LAST:event_tb_SPMouseClicked

