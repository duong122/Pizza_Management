    private void showDataSP(String query){
        List<SanPhamnam> lsp = new ArrayList<>();
        ResultSet rs = kn.LayDuLieu(query);
        try{
            while(rs.next()){
                SanPhamnam sp = new SanPhamnam(rs.getString("MaSP"), rs.getString("TenSP"), rs.getFloat("DonGia"));
                lsp.add(sp);
            }
            
        DefaultTableModel tableModel;
        tableModel = (DefaultTableModel)tb_SP.getModel();
        tableModel.setRowCount(0);
        lsp.forEach((x)->{
            tableModel.addRow(new Object[] {
                x.getMaSP(), x.getTenSP(), x.getDonGia()
            });
        });
        }
        catch(SQLException e){
            
        }
    }
    
