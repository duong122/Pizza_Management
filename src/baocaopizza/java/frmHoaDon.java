    private void mniXoaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mniXoaSPActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbModel = (DefaultTableModel) tb_HoaDon.getModel();
        int viTri = tb_HoaDon.getSelectedRow();
        if(viTri >= 0){
            tbModel.removeRow(viTri);
            for(int i=viTri;i<tb_HoaDon.getRowCount();i++){
                tb_HoaDon.setValueAt(i+1, i, 1);
            }
        }
        txtTongTien.setText(String.valueOf(tongTien()));
    }//GEN-LAST:event_mniXoaSPActionPerformed

